public class Nestedforloop 
{
public static void main(String[] args) 
{
System.out.println("main method started");	

for(int i=0;i<5;i++)
{
	for(int j=5;j>0;j--)
	{
		System.out.println("happy indeppendance day");
	}
}

System.out.println("main method ended");
}
}
