package com.August;

public class Switch 
{
public static void main(String[] args) 
{
System.out.println("main method started");

int i=5;
switch(i)
{
case 1 :System.out.println("integer");
break;

case 2 : System.out.println("boolean");
break;

case 3 :System.out.println("long");
break;

case 4:System.out.println("float");
break;

default :System.out.println("error accur");


}

System.out.println("main method ended");
}
}
