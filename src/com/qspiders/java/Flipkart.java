package com.qspiders.java;

public class Flipkart 
{
private int orders;
private String accName;
private int orderCancel;
public Flipkart(int orders, String accName, int orderCancel)
{
	this.orders=orders;
	this.orderCancel=orderCancel;
	this.accName=accName;
}
public int getSelling()
{
	return orders=orders;
}
public String getCustomer()
{
	return accName=accName;
}
public int getReturning()
{
	return orderCancel=orderCancel;
}
public void setSelling(int orders)
{
	this.orders=orders;
}
public void setCustomer(String accName)
{
	this.accName=accName;
}
public void setReturning(int orderCancel)
{
	this.orderCancel=orderCancel;
}
}
